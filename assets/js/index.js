var b_count = 0;
const btn_b = document.getElementById('beef');
btn_b.addEventListener('click', function () {
    if (b_count == 0){
        
        btn_b.style.backgroundColor = 'white';
        btn_b.style.border = '3px dashed black';
        btn_b.style.color = 'white';
        b_count = 1;
        
        //could possibly set the value for db querry here as well 
    }
    else{
        
        btn_b.style.backgroundColor = 'white';
        btn_b.style.border = '3px dashed black';
        btn_b.style.color = 'white';
        b_count = 0;
        
    }
});

